package projeto;

public class Mensagem {

	private String idSessao, destinatario, assunto, mensagem;

	public Mensagem(String idSessao, String destinatario, String assunto,
			String mensagem) {
		this.idSessao = idSessao;
		this.destinatario = destinatario;
		this.assunto = assunto;
		this.mensagem = mensagem;
	}

}
